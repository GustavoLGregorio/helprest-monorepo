/**
 * patch-android-signing.js
 *
 * Patches android/app/build.gradle and android/app/src/main/res/values/styles.xml
 * after `expo prebuild --clean` to restore:
 *   1. The project keystore (keys/keystore.jks) in the signingConfigs block.
 *   2. The Theme.EdgeToEdge style definition that expo prebuild references but
 *      does not define (normally provided by react-native-edge-to-edge).
 *
 * This script runs automatically after `prebuild:android` and `nuke`.
 */

const fs = require("fs");
const path = require("path");

const BUILD_GRADLE_PATH = path.join(
    __dirname,
    "..",
    "android",
    "app",
    "build.gradle",
);

// Signing config block generated by expo prebuild (target to replace)
const DEFAULT_SIGNING_BLOCK = `    signingConfigs {
        debug {
            storeFile file('debug.keystore')
            storePassword 'android'
            keyAlias 'androiddebugkey'
            keyPassword 'android'
        }
    }`;

// Correct signing config using the project keystore
const PROJECT_SIGNING_BLOCK = `    signingConfigs {
        debug {
            // Using the project keystore so the SHA-1 matches what is
            // registered in Google Cloud Console for Google Sign-In.
            // The default debug.keystore (generated by prebuild) has a
            // different SHA-1 and causes DEVELOPER_ERROR on Google Sign-In.
            storeFile file('../../keys/keystore.jks')
            storePassword '74bf17c9dd20d10911aa48e1490f8171'
            keyAlias '0d33bb73fe7a6b8ae839b001b6696f06'
            keyPassword '619e9a2516524d7d30f3f7b7aa249bde'
        }
    }`;

function patchSigning() {
    if (!fs.existsSync(BUILD_GRADLE_PATH)) {
        console.error("[patch-signing] build.gradle not found. Run expo prebuild first.");
        process.exit(1);
    }

    let content = fs.readFileSync(BUILD_GRADLE_PATH, "utf8");

    if (content.includes("keystore.jks")) {
        console.log("[patch-signing] ✅ build.gradle already uses the project keystore. No patch needed.");
        return;
    }

    if (!content.includes(DEFAULT_SIGNING_BLOCK.trim().split("\n")[1].trim())) {
        console.warn("[patch-signing] ⚠️  Could not find the default signingConfigs block. Skipping patch.");
        console.warn("[patch-signing]    Manually verify android/app/build.gradle signingConfigs.");
        return;
    }

    content = content.replace(DEFAULT_SIGNING_BLOCK, PROJECT_SIGNING_BLOCK);
    fs.writeFileSync(BUILD_GRADLE_PATH, content, "utf8");
    console.log("[patch-signing] ✅ Patched android/app/build.gradle with project keystore.");
}

// ─── Patch 2: styles.xml — define missing Theme.EdgeToEdge ───────────────────

const STYLES_XML_PATH = path.join(
    __dirname,
    "..",
    "android",
    "app",
    "src",
    "main",
    "res",
    "values",
    "styles.xml",
);

const EDGE_TO_EDGE_STYLE = `
  <!--
    Theme.EdgeToEdge: base theme required by Expo's edgeToEdgeEnabled flag.
    Normally provided by react-native-edge-to-edge; defined here directly
    to avoid adding an extra dependency solely for this style definition.
  -->
  <style name="Theme.EdgeToEdge" parent="Theme.AppCompat.DayNight.NoActionBar">
    <item name="android:navigationBarColor">@android:color/transparent</item>
    <item name="android:statusBarColor">@android:color/transparent</item>
    <item name="android:windowTranslucentNavigation">false</item>
    <item name="android:windowTranslucentStatus">false</item>
    <item name="android:windowDrawsSystemBarBackgrounds">true</item>
  </style>`;

function patchStyles() {
    if (!fs.existsSync(STYLES_XML_PATH)) {
        console.warn("[patch-styles] styles.xml not found. Skipping.");
        return;
    }

    let content = fs.readFileSync(STYLES_XML_PATH, "utf8");

    if (content.includes('name="Theme.EdgeToEdge"')) {
        console.log("[patch-styles] ✅ Theme.EdgeToEdge already defined. No patch needed.");
        return;
    }

    // Insert the Theme.EdgeToEdge style right after the <resources ...> opening tag
    content = content.replace(
        /(<resources[^>]*>)/,
        `$1${EDGE_TO_EDGE_STYLE}\n`,
    );

    fs.writeFileSync(STYLES_XML_PATH, content, "utf8");
    console.log("[patch-styles] ✅ Patched styles.xml with Theme.EdgeToEdge definition.");
}

// ─── Run all patches ──────────────────────────────────────────────────────────

patchSigning();
patchStyles();
